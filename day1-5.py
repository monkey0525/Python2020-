point=input('輸入分數')
point=int(point) 
if point>=0 and point<=100:
    if point>=90:
        print('A')
    elif point>=80:
        print('B')
    elif point>=70:
        print('c')
    elif point>=60:
        print('D')
    else :
        print('E')
else :
    print('輸入錯誤')
    
        






